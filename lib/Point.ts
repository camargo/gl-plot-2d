
/**
 * Point.
 *
 * @export
 * @class Point
 */
export class Point {
  public x: number;
  public y: number;

  /**
   * Creates an instance of Point.
   *
   * @param {number} x
   * @param {number} y
   *
   * @memberOf Point
   */
  constructor(x: number, y: number) {
    this.x = x;
    this.y = y;
  }
}
